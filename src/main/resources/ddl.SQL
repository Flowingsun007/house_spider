CREATE TABLE `house` (
  `id` varchar(15) NOT NULL DEFAULT '' COMMENT '房源编号id',
  `title` varchar(255) DEFAULT NULL COMMENT '标题',
  `url` varchar(255) DEFAULT NULL COMMENT '链接',
  `city` varchar(20) DEFAULT NULL COMMENT '城市',
  `region` varchar(20) DEFAULT NULL COMMENT '行政区域',
  `street` varchar(30) DEFAULT NULL COMMENT '街道',
  `community` varchar(50) DEFAULT NULL COMMENT '小区',
  `floor` varchar(255) DEFAULT NULL COMMENT '楼层',
  `total_price` double DEFAULT NULL COMMENT '总价(万)',
  `average_price` double DEFAULT NULL COMMENT '均价(元/平米)',
  `image` varchar(255) DEFAULT NULL COMMENT '首图链接',
  `watch` int(1) DEFAULT NULL COMMENT '关注',
  `view` int(11) DEFAULT NULL COMMENT '带看次数',
  `release_date` varchar(30) DEFAULT NULL COMMENT '发布时间',
  `room_count` varchar(30) DEFAULT NULL COMMENT '房间数量',
  `towards` varchar(30) DEFAULT NULL COMMENT '朝向',
  `house_area` double DEFAULT NULL COMMENT '面积(平米)',
  `decoration` varchar(20) DEFAULT NULL COMMENT '装修',
  `elevator` varchar(20) DEFAULT NULL COMMENT '电梯',
  `create_date` timestamp NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;